import sys
import re

def main(text, language):
    """
    Formats the forest plot description by inserting citations and appending legends.
    
    Args:
        text (str): The description text generated by the LLM.
        language (str): 'Chinese' or 'English'.
    
    Returns:
        str: The formatted text.
    """
    # 1. Insert (Figure 2) before the last punctuation
    # Match standard and CJK punctuation
    punctuation_pattern = r'[.!?。！？]'
    
    # Search in reverse to find the last punctuation
    # We reverse the string, find the first match, then calculate index
    reversed_text = text[::-1]
    match = re.search(punctuation_pattern, reversed_text)
    
    modified_text = text
    if match:
        # Index in reversed string
        rev_start = match.start()
        # Index in original string = len - 1 - rev_start
        last_punct_index = len(text) - 1 - rev_start
        
        # Insert before the punctuation
        modified_text = text[:last_punct_index] + "(Figure 2)" + text[last_punct_index:]
    else:
        # No punctuation found, append to end
        modified_text = text + "(Figure 2)"

    # 2. Add Header and Footer based on language
    header = ""
    footer = ""
    
    if language == "Chinese":
        header = "**Forest Map**"
        footer = """{Insert your picture here}

 **Figure 2 Forest plot of combined effect sizes**"""
    else:
        # Default to English
        header = "**Forest Plot**\n\n"
        footer = """

{Insert your picture here}

 **Figure 2 Forest plot of the pooled effect size**"""
        
    final_output = header + modified_text + footer
    return final_output

if __name__ == "__main__":
    # Simple CLI for testing or usage
    if len(sys.argv) < 3:
        print("Usage: python format_result.py <text> <language>")
        sys.exit(1)
        
    input_text = sys.argv[1]
    input_lang = sys.argv[2]
    
    print(main(input_text, input_lang))
